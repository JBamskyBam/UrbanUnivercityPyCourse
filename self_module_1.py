# 1. Проверка возраста
# Напишите программу, которая принимает возраст пользователя и выводит сообщение о том,
# может ли он голосовать (18 лет и старше), получить водительские права (16 лет и старше),
# или слишком молод (меньше 16 лет).

age = int(input('Введите возраст: '))
if age >= 16 and age < 18:
    print(
        f'Вам уже {age} лет (больше 16 лет), значит вы можете получить права, но меньше 18 лет, вы не можете голосовать.')
elif age >= 18:
    print(f'Вам уже {age} лет (больше 18 лет), значит вы можете и получить права и голосовать.')
else:
    print('Ксожалению вы не можете ни получить права ни голосовать.')

