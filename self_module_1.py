# 6. Калькулятор
# Напишите программу, которая принимает два числа и символ операции (+, -, *, /) и выводит результат выполнения операции.

num1 = int(input('Введите первое число: '))
num2 = int(input('Введите второе число'))
oper = input('Введите символ операции (+, -, *, /)')
if oper == '+':
    print(num1 + num2)
elif oper == '-':
    print(num1 - num2)
elif oper == '*':
    print(num1 * num2)
elif oper == '/':
    print(num1 / num2)

# 7. Обратный отсчет
# Напишите программу, которая принимает число и выполняет обратный отсчет до 0, выводя каждое значение.

a = int(input('Введите любое число, как положительное так и отрицательное: '))
while a > 0:
    print(a)
    a = a - 1
    if a == 0:
        print('конец')
while a < 0:
    print(a)
    a = a + 1
    if a == 0:
        print('конец')

